#Stage 1: Basic Calculator
def calculator(num1,num2,operator):
  result=0
  if operator == "+":
    result = num1 + num2

  elif operator == "/":
    if num2 ==0:
      print(f"num2 is not allowed the zero. division by zero")
    else:
      result = num1 / num2
     # print("Result =" result)

  elif operator == "-":
    result = num1 - num2
   # print("Result =" result)

  elif operator == "*":
    result = num1 * num2
   # print("Result =" result)

  else:
    print(f"opperator error")

  return result

#Stage 2: Add Result Check

def check_number(num1,num2,operator):
  result =calculator(num1,num2,operator)
  if result > 0:
    check="Positive"
  elif result < 0:
    check="Negative"
  else:
    check="Zero"
  return  result,check

