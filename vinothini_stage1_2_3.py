"""Stage 3: Student Grade Calculator
Task: Calculate student grade based on marks in 3 subjects.

Input: Student name, 3 subject marks (0-100)

Calculate:

Total marks
Percentage = (total/300) * 100
Grade Logic:

A: >= 75%
B: >= 60%
C: >= 40%
F: < 40%
Output: Name, Total, Percentage, Grade


"""

def grade_calculator(sub1,sub2,sub3):
  total=sub1+sub2+sub3
  percentage=(total/300)*100
  if percentage >= 75:
    grade="A"
  elif percentage >=60:
    grade="B"
  elif percentage >=40:
    grade="C"
  else:
    grade="F"

  return total,percentage,grade

name=input(f"Enter the name: ")
sub1=int(input(f"Enter the marks of sub1: "))
sub2=int(input(f"Enter the marks of sub2: "))
sub3=int(input(f"Enter the marks of sub3: "))
total_mark,percentage,grade=grade_calculator(sub1,sub2,sub3)
print(f"Name: {name}\nTotal: {total_mark}\nPercentage: {percentage}\nGrade: {grade}")